package Pruebas01;

public class EjercicioClase01 {

	public static void main(String[] args) {
		//imprimir por la pantalla usando un bucle while el cuadrado de los primeros 10 numeros
		
		//comenzamos con el numero 0 creando la variable numero
		int numero=0;
		
		//creamos un bucle while empezando a delimitar hasta donde queremos que recorra
		while (numero<=10) {
			//ponemos en cuanto incrementa el numero
		numero++;
		//imprimir la multiplicacion
		System.out.println(numero*numero);
		}
	}
	
	

}
